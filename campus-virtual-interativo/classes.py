class Evento:
    def __init__(self, id, nome, descricao, palestrante, local, urlMapa, imagem, dia, mes):
        self.id = id
        self.nome = nome
        self.descricao = descricao
        self.palestrante = palestrante
        self.local = local
        self.urlMapa = urlMapa
        self.imagem = imagem
        self.dia = dia
        self.mes = mes